    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
       // NumberFormatException
        try {
            String Name = this.nome.getText();
            String Country = this.pais.getText();
            String sex = (String)this.sexo.getSelectedItem();
            Double Time = Double.valueOf(this.tempo.getText());
            Atleta t1;
        if(!("".equals(Name)) && !("".equals(Country)) && !("Selecione:".equals(sex))){
            if(sex.equalsIgnoreCase("masculino")){
            t1 = new Atleta(Country, Time, Name, 'M');
            f1.enqueue(t1);
        }else{
            t1 = new Atleta(Country, Time, Name, 'F');
            p1.push(t1);
        }
        JOptionPane.showMessageDialog(null, "Cadastro feito com sucesso");
            nome.setText("");
            pais.setText("");
            sexo.setSelectedIndex(0);
            tempo.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todas as informações");
        }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um tempo válido");
        }
    }//GEN-LAST:event_cadastrarActionPerformed

    private void listaGeralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listaGeralActionPerformed
        Atleta[] listaFem =  (Atleta[]) p1.toArray();
        Atleta[] listaMas =  (Atleta[]) f1.toArray();             
        String msg = "Mulheres: \n";
        if(listaFem!= null){
            bubbleSort(listaFem);
            for (int i = 0; i < listaFem.length; i++) {
            msg += listaFem[i].toString();
            if(i!= listaFem.length - 1){
                msg+= "\n";
            }
        }
        }else{
            msg+= "Pilha vazia";
        }
        msg += "\nHomens: \n";
        if(listaMas != null){
            bubbleSort(listaMas);
            for (int i = 0; i < listaMas.length; i++) {
            msg += listaMas[i].toString();
            if(i!= listaMas.length - 1){
                msg+= "\n";
            }
        }
        }else{
            msg+= "Fila vazia";
        }
        jTextArea1.setText(msg);
    }//GEN-LAST:event_listaGeralActionPerformed

    private void mediaGeralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mediaGeralActionPerformed
        
        Double media = 0.0;
        Atleta[] listaFem = p1.toArray();
        Atleta[] listaMas = f1.toArray();
        String msg = "";
        if(listaFem == null){
            msg+="\n A pilha está vazia";
        }
        if(listaMas == null){
            msg +=" \n A fila está vazia";
        }
        if(listaFem == null){
            listaFem = new Atleta[0];
        }
        if(listaMas == null){
            listaMas = new Atleta[0];
        }
        Integer total = listaFem.length+listaMas.length;
        for (int i = 0; i < listaFem.length; i++) {
            media += listaFem[i].getTempo();
        }
        for (int i = 0; i < listaMas.length; i++) {
            media += listaMas[i].getTempo();
        }
        media = media/total;
        Double result = (double)Math.round(media*100)/100;
        msg += "\nA média dos tempos é: "+ result;
        

        JOptionPane.showMessageDialog(null, msg );
    }//GEN-LAST:event_mediaGeralActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Atleta[] lista = p1.toArray();
        if(lista == null){
            JOptionPane.showMessageDialog(null, "Não há mulheres cadastradas");
        }else{
            double menor = lista[0].getTempo();
            int posicao = 0;
            for (int i = 0; i < lista.length; i++) {
                if(lista[i].getTempo() < menor){
                    menor = lista[i].getTempo();
                    posicao = i;
                }
            }
            String msg = "\nA mulher com melhor tempo é: \n" + lista[posicao].toString();
            jTextArea1.setText(msg); 
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void mediaHomensActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mediaHomensActionPerformed
        Atleta[] lista = f1.toArray();
        if(lista == null){
            JOptionPane.showMessageDialog(null, "Não há homens cadastrados");
        }else{
            double soma = 0;
            int contador = 0;
            for (int i = 0; i < lista.length; i++) {
                if(lista[i].getPais().equalsIgnoreCase("brasil")){
                    soma+= lista[i].getTempo();
                    contador+=1;
                }
            }
            JOptionPane.showMessageDialog(null, "A média dos homens brasileiros é: "+ (soma/contador));
        }
        
    }//GEN-LAST:event_mediaHomensActionPerformed

