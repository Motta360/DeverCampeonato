    public void bubbleSort(Atleta[] x){
        for (int i = 0; i < x.length - 1; i++) {
            for (int j = 0; j < x.length-1-i; j++) {
                if(x[j].getName().compareToIgnoreCase(x[j+1].getName()) > 0){
                    Atleta temp = x[j];
                    x[j] = x[j+1];
                    x[j+1] = temp;
                }
            }
        }
    }

